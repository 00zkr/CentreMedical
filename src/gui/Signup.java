    private void btnSignupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignupActionPerformed
        // TODO add your handling code here:
        User user = new User(txtEmail.getText(), txtNom.getText(), txtMotpasse.getText());
        boolean signupSuccess = userDao.signup(user);
        MessageDialog dialog = new MessageDialog(this);
        if (signupSuccess) {
            dialog.showMessage("Signup successful!");
        } else {
            dialog.showMessage("Signup failed. Email may already exist.");
        }
        clear();
        
        
    }//GEN-LAST:event_btnSignupActionPerformed

