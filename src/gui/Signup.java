    public void validateFields(){
        String nom = txtNom.getText();
        String email = txtEmail.getText();
        String password = txtMotpasse.getText();
        String password2 = txtMotpasse2.getText();
        
        if (!nom.isEmpty() && email.matches(emailpattern) && !password.isEmpty() && password.equals(password2)){
            btnSignup.setEnabled(true);
        }
    }

