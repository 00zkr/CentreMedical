    private void loadMedcins() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for (Medcin m : dao.findAll()) {
            model.addRow(new Object[]{
                m.getId(),
                m.getNom(),
                m.getSpecialite(),
                m.getTelephone()
            });
    }}

