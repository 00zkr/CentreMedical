    private void btnModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifierActionPerformed
        // TODO add your handling code here:
        Medcin selectedMedcin = dao.findById(id);
        if (selectedMedcin != null) {
            MedecinDialog dialog = new MedecinDialog((JFrame) SwingUtilities.getWindowAncestor(this), selectedMedcin);
            dialog.setVisible(true);

            loadMedcins();
        } else {
            JOptionPane.showMessageDialog(this, "Please select a mededin first.");
        }
    }//GEN-LAST:event_btnModifierActionPerformed

    private void btnSupprActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSupprActionPerformed
        // TODO add your handling code here:
        int confirm = JOptionPane.showConfirmDialog(this, 
            "Etes vous sure de supprimer ce medecin?", 
            "Confirmer suppression", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            Medcin m = dao.findById(id);
            if (m != null && dao.delete(m)) {
                JOptionPane.showMessageDialog(this, "Medecin supprimé.");
                loadMedcins();
            } else {
                JOptionPane.showMessageDialog(this, "Medcin pas supprimé.");
            }
        }
        
    }//GEN-LAST:event_btnSupprActionPerformed

